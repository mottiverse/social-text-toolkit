/**
 * Search dividers across all categories.
 * @param {string} query - Search query
 * @returns {Array} Matching dividers with their category info
 */
export function searchDividers(query) {
  const q = query.toLowerCase().trim();
  if (!q) return [];

  const results = [];
  for (const category of dividerCategories) {
    if (category.name.toLowerCase().includes(q) || category.id.includes(q)) {
      for (const divider of category.dividers) {
        results.push({ divider, category: category.id, categoryName: category.name });
      }
    }
  }
  return results;
}
